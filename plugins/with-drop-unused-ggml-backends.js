// Expo config plugin: keep the unused ggml GPU backends out of the Android APK.
//
// @qvac/llm-llamacpp ships one shared library per ggml compute backend, and
// bare-link copies the whole prebuilds directory in, so ALL of them land in the
// APK. Two are pure dead weight:
//
//   libqvac-ggml-vulkan.so   108 MB   (~106 MB of precompiled SPIR-V shaders)
//   libqvac-ggml-opencl.so   1.8 MB
//
// They are dead because app/qvac.ts loads the model with `device: 'cpu'`, so
// only the libqvac-ggml-cpu-* slices are ever used. Shipping them took the
// release APK from 118 MB (v1.0.0, pre-AI) to 259 MB (v1.0.1). Dropping them
// brings it back to roughly 137 MB with no behavior change.
//
// Why this is safe: libqvac__llm-llamacpp.so does NOT list either backend as a
// DT_NEEDED, so nothing fails to link. ggml discovers backends by dlopen'ing
// them from the library directory at runtime and registers whatever it finds.
// A backend that is not there is simply not registered, which is the same state
// as a device whose driver rejects it. If we ever want GPU inference, revert
// this plugin AND change the `device` in app/qvac.ts - one without the other is
// what created the problem in the first place.
//
// This is a plugin rather than a build.gradle edit because android/ is
// gitignored and regenerated by `expo prebuild`. It cannot be done by trimming
// qvac/addons.manifest.json either: that allowlist is per PACKAGE, and the
// backends ride along inside @qvac/llm-llamacpp, which we do need.

const { withAppBuildGradle } = require('expo/config-plugins')

// Path globs, matched against the archive entry (e.g. /lib/arm64-v8a/foo.so).
const EXCLUDES = [
  '/lib/**/libqvac-ggml-vulkan.so',
  '/lib/**/libqvac-ggml-opencl.so',
]

module.exports = function withDropUnusedGgmlBackends (config) {
  return withAppBuildGradle(config, (cfg) => {
    const contents = cfg.modResults.contents
    if (contents.includes(EXCLUDES[0])) return cfg

    // The `jniLibs {` inside `packagingOptions {`. Line-anchored, and the only
    // such line in app/build.gradle - @qvac/sdk's own withOpenCL plugin already
    // injects its libOpenCL.so exclude into this same block.
    const anchor = /^(\s*)jniLibs \{$/m
    if (!anchor.test(contents)) {
      // Fail loudly. Silently packaging the backends again would only show up
      // as a quietly 110 MB larger APK, which is exactly what went unnoticed
      // between v1.0.0 and v1.0.1.
      throw new Error(
        'with-drop-unused-ggml-backends: could not find the `jniLibs {` block ' +
        'in app/build.gradle. The Expo/AGP template changed; update the anchor, ' +
        'or the unused 110 MB of ggml GPU backends will silently ship again.'
      )
    }

    const lines = EXCLUDES.map((glob) => `\n$1    excludes += "${glob}"`).join('')
    cfg.modResults.contents = contents.replace(
      anchor,
      '$1jniLibs {' +
      '\n$1    // See plugins/with-drop-unused-ggml-backends.js: unused ggml GPU' +
      '\n$1    // backends (~110 MB). app/qvac.ts runs the model on the CPU.' +
      lines
    )
    return cfg
  })
}
