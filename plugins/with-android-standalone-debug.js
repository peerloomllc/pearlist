// Expo config plugin: make the Android DEBUG build standalone - the JS bundle is
// packaged into the APK, so the app does not need a Metro dev server.
//
// Why (CLAUDE.md rule 5): debug builds are sideloaded onto real test phones and
// have to Just Work untethered. React Native's gradle plugin skips bundling for
// its `debuggableVariants` (default: ["debug"]), so a stock debug APK expects
// Metro and shows "Unable to load script" in the field, which makes it useless
// for on-device testing. Setting the list to EMPTY means no variant is treated
// as debuggable-with-Metro, so debug bundles its JS exactly like release does.
//
// This is a plugin rather than a build.gradle edit because android/ is gitignored
// and regenerated by `expo prebuild`, so a hand-edit is wiped every time. It was
// re-applied by hand at least five times during the 2026-07-21 note-lists and
// WebView-freeze work before this existed.
//
// Pairs with with-android-debug-app-id (the .debug applicationId suffix). Between
// them a debug APK is standalone AND installs alongside the release app.
//
// Trade-off, deliberate: with the JS embedded, a debug build no longer picks up
// live edits from Metro. `npx expo start` for hot reload during UI work; this
// affects the APK you sideload, which is what the field testing rule is about.

const { withAppBuildGradle } = require('expo/config-plugins')

const SETTING = 'debuggableVariants = []'

module.exports = function withAndroidStandaloneDebug (config) {
  return withAppBuildGradle(config, (cfg) => {
    const contents = cfg.modResults.contents
    if (contents.includes(SETTING)) return cfg

    // Anchor on the top-level `react {` block opened by the RN gradle plugin.
    // Line-anchored so it cannot match the word inside a comment or a nested
    // block, and it appears exactly once in app/build.gradle.
    const anchor = /^react \{$/m
    if (!anchor.test(contents)) {
      // Fail loudly. Silently producing a Metro-dependent debug APK is the exact
      // failure this plugin exists to prevent, and it would only surface later as
      // "Unable to load script" on a test device.
      throw new Error(
        'with-android-standalone-debug: could not find the `react {` block in ' +
        'app/build.gradle. The React Native gradle template changed; update the ' +
        'anchor, or debug builds will silently go back to needing Metro.'
      )
    }

    cfg.modResults.contents = contents.replace(
      anchor,
      'react {\n    // See plugins/with-android-standalone-debug.js: empty so the debug\n' +
      '    // variant BUNDLES its JS and runs with no Metro server (CLAUDE.md rule 5).\n' +
      `    ${SETTING}`
    )
    return cfg
  })
}
