// Expo config plugin: compress the native libraries in the DEBUG APK.
//
// Every .so ships uncompressed by default (expo.useLegacyPackaging=false in
// gradle.properties), so Android can map it straight out of the APK. That is the
// right trade for the Play AAB, where uncompressed means a smaller INSTALL. It is
// the wrong trade for a debug build, which exists to be moved: over USB to the TCL,
// over Taildrop to the Pixel, many times a day.
//
// Measured 2026-07-23 on arm64: 183.4 MB -> 93.0 MB. The libs go 122.7 MB raw to
// 41.2 MB stored, with libbare-kit.so alone 60.1 -> 18.5 MB, because it is V8 and
// V8 compresses about 3.3x. The cost is a slower install and a bigger on-disk
// footprint, neither of which matters for a test build.
//
// Why the Variant API rather than the gradle property: `expo.useLegacyPackaging`
// is global, so setting it would also change the release AAB. AGP's
// androidComponents selector scopes this to the debug build type alone. The
// release APK gets the same treatment from the other direction, via
// -Pexpo.useLegacyPackaging=true on the assembleRelease call in scripts/release.sh,
// which leaves the AAB in the same script untouched.
//
// A plugin rather than a build.gradle edit because android/ is gitignored and
// regenerated by `expo prebuild`. Same reason as with-drop-unused-ggml-backends.

const { withAppBuildGradle } = require('expo/config-plugins')

const MARKER = 'with-compressed-debug-libs'

const BLOCK = `
// See plugins/${MARKER}.js: compress native libs in DEBUG only (183 MB -> 93 MB).
// Scoped with the Variant API because expo.useLegacyPackaging is global and would
// otherwise also change the release AAB, where uncompressed means a smaller install.
androidComponents {
    onVariants(selector().withBuildType("debug")) { variant ->
        variant.packaging.jniLibs.useLegacyPackaging.set(true)
    }
}
`

module.exports = function withCompressedDebugLibs (config) {
  return withAppBuildGradle(config, (cfg) => {
    if (cfg.modResults.contents.includes(MARKER)) return cfg
    if (cfg.modResults.language !== 'groovy') {
      // Fail loudly rather than silently shipping a 183 MB debug APK again.
      throw new Error(
        `${MARKER}: app/build.gradle is ${cfg.modResults.language}, not groovy. ` +
        'Port the androidComponents block before this build type stops being compressed.'
      )
    }
    cfg.modResults.contents += BLOCK
    return cfg
  })
}
